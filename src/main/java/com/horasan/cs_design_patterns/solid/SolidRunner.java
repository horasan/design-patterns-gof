package com.horasan.cs_design_patterns.solid;

public class SolidRunner {
	/*
	 	1) 	Single Responsibility
	 		Each class must have only one reason to change.
	 	2)	Open-Closed Principle
	 	3)	Liskov Substitution Principle
	 		You must be able to substitute a sub class for a base class.
	 	4)	Interface Segregation Principle
	 		Derived classes should not have issue with implementing a behaviour.
	 		Have multiple interface.  
 		5)	Dependency Inversion Principle
 			5.1)	High-level modules should not depend on low-level modules
 					Both should depend on abstractions.
 			5.2)	Abstractions should not depend on details.
 			 		Details should depend on abstractions.
	 */
}
